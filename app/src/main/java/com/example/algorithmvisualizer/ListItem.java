package com.example.algorithmvisualizer;

public class ListItem {
    private String name;
    private String desc;
    public ListItem(String name,String desc)
    {
        this.name=name;
        this.desc=desc;
    }
    public String getname()
    {
        return this.name;
    }
    public String getdesc()
    {
        return this.desc;
    }
}
